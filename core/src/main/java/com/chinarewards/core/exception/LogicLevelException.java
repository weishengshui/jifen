package com.chinarewards.core.exception;

public class LogicLevelException extends Exception {

	private static final long serialVersionUID = -9191668766860119680L;

	public LogicLevelException() {
		super();
	}

	public LogicLevelException(String message, Throwable cause) {
		super(message, cause);
	}

	public LogicLevelException(String message) {
		super(message);
	}

	public LogicLevelException(Throwable cause) {
		super(cause);
	}

}
